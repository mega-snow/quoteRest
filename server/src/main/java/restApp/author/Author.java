@Entity
public class Author {
    @Id    
    //@TableGenerator(name="AuthorIdGen",table="author",pkColumnName="id", allocationSize=1)
    @GeneratedValue(strategy = GenerationType.IDENTITY)    
    private Long id;
    
    @Column(unique = true, nullable = false)
    String name;
    
    public Author(){}
    
    public Author(String name){
        this.name = name;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }
    
    

    @Override
    public int hashCode() {
        int hash = 7;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Author other = (Author) obj;
        if (!Objects.equals(this.id, other.id)) {
            return false;
        }
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        return true;
    }
    
    
    
}
